func wordBreakFSA(runes []rune) (breaks []int) {
	// http://www.unicode.org/reports/tr29/#Word_Boundaries
	// http://www.unicode.org/Public/UNIDATA/auxiliary/WordBreakTest.html
	//3.0: CR × LF
	//4.0: [^ Newline CR LF ] × [Format Extend]
	//5.0: AHLetter × AHLetter
	//6.0: AHLetter × (MidLetter | MidNumLetQ) AHLetter
	//7.0: AHLetter (MidLetter | MidNumLetQ) × AHLetter
	//7.1: Hebrew_Letter × Single_Quote
	//7.2: Hebrew_Letter × Double_Quote Hebrew_Letter
	//7.3: Hebrew_Letter Double_Quote × Hebrew_Letter
	//8.0: Numeric × Numeric
	//9.0: AHLetter × Numeric
	//10.0: Numeric × AHLetter
	//11.0: Numeric (MidNum | MidNumLetQ) ×	Numeric
	//12.0: Numeric × (MidNum | MidNumLetQ) Numeric
	//13.0: Katakana × Katakana
	//13.1: (AHLetter | Numeric | Katakana | ExtendNumLet) × ExtendNumLet
	//13.2: ExtendNumLet × (AHLetter | Numeric | Katakana)
	//13.3: Regional_Indicator × Regional_Indicator
	var (
		state_3_0_a state
		state_3_0_b state
		state_5_0_a state
		state_5_0_b state
		state_6_0   state
		state_7_2   state
		state_8_0   state
		state_11_0  state
		state_13_0  state
		state_13_1  state
		state_13_3  state
	)
	state_3_0_a = state{
		name: "3_0_a",
		next: []transition{
			{in: lf, to: &state_3_0_b},
		},
	}
	state_3_0_b = state{
		name: "3_0_b",
	}
	state_5_0_a = state{
		name: "5_0_a",
		next: []transition{
			{in: aletter, to: &state_5_0_a},
			{in: numeric, to: &state_8_0},
			{in: singleQuote, to: &state_6_0},
			{in: midLetter, to: &state_6_0},
			{in: midNumLet, to: &state_6_0},
			{in: hebrewLetter, to: &state_5_0_b},
			{in: extendNumLet, to: &state_13_1},
			{in: format, to: &state_5_0_a},
			{in: extend, to: &state_5_0_a},
		},
	}
	state_5_0_b = state{
		name: "5_0_b",
		next: []transition{
			{in: aletter, to: &state_5_0_a},
			{in: numeric, to: &state_8_0},
			{in: singleQuote, to: &state_6_0},
			{in: doubleQuote, to: &state_7_2},
			{in: midLetter, to: &state_6_0},
			{in: midNumLet, to: &state_6_0},
			{in: hebrewLetter, to: &state_5_0_b},
			{in: extendNumLet, to: &state_13_1},
			{in: format, to: &state_5_0_b},
			{in: extend, to: &state_5_0_b},
		},
	}
	state_6_0 = state{
		name: "6_0",
		next: []transition{
			{in: aletter, to: &state_5_0_a},
			{in: numeric, to: &state_8_0},
			{in: hebrewLetter, to: &state_5_0_b},
			{in: extendNumLet, to: &state_13_1},
			{in: format, to: &state_6_0},
			{in: extend, to: &state_6_0},
		},
	}
	state_7_2 = state{
		name: "7_2",
		next: []transition{
			{in: hebrewLetter, to: &state_5_0_b},
			{in: format, to: &state_7_2},
			{in: extend, to: &state_7_2},
		},
	}
	state_8_0 = state{
		name: "8_0",
		next: []transition{
			{in: aletter, to: &state_5_0_a},
			{in: numeric, to: &state_8_0},
			{in: singleQuote, to: &state_6_0},
			{in: midNumLet, to: &state_6_0},
			{in: midNum, to: &state_11_0},
			{in: hebrewLetter, to: &state_5_0_b},
			{in: format, to: &state_8_0},
			{in: extend, to: &state_8_0},
		},
	}
	state_11_0 = state{
		name: "11_0",
		next: []transition{
			{in: numeric, to: &state_8_0},
			{in: format, to: &state_11_0},
			{in: extend, to: &state_11_0},
		},
	}
	state_13_0 = state{
		name: "13_0",
		next: []transition{
			{in: katakana, to: &state_13_0},
			{in: extendNumLet, to: &state_13_1},
			{in: format, to: &state_13_0},
			{in: extend, to: &state_13_0},
		},
	}
	state_13_1 = state{
		name: "13_1",
		next: []transition{
			{in: aletter, to: &state_5_0_a},
			{in: numeric, to: &state_8_0},
			{in: katakana, to: &state_13_0},
			{in: hebrewLetter, to: &state_5_0_b},
			{in: extendNumLet, to: &state_13_1},
			{in: format, to: &state_13_1},
			{in: extend, to: &state_13_1},
		},
	}
	state_13_3 = state{
		name: "13_3",
		next: []transition{
			{in: regionalIndicator, to: &state_13_3},
			{in: format, to: &state_13_3},
			{in: extend, to: &state_13_3},
		},
	}

	start := state{
		name: "start",
		next: []transition{
			{in: cr, to: &state_3_0_a},
			{in: aletter, to: &state_5_0_a},
			{in: numeric, to: &state_8_0},
			{in: katakana, to: &state_13_0},
			{in: hebrewLetter, to: &state_5_0_b},
			{in: extendNumLet, to: &state_13_1},
			{in: regionalIndicator, to: &state_13_3},
			{in: format, to: nil},
			{in: extend, to: nil},
		},
	}

	var s *state
	for i, r := range runes {
		if s == nil {
			breaks = append(breaks, i)
			s = &start
		}
		//fmt.Printf("Advancing state from %-5s ", s.name)
		s = s.advance(unicodeRange(r))
		if blen := len(breaks); s == nil && blen > 0 && breaks[blen-1] != i {
			breaks = append(breaks, i)
		}
		//if s != nil {
		//	fmt.Printf("to %-5s on rune %c\n", s.name, r)
		//} else {
		//	fmt.Printf("to #end# on rune %c\n", r)
		//}
	}
	return breaks
}

type transition struct {
	in rangeTableId //input
	to *state
}
type state struct {
	name string
	next []transition //for <= 10 elements slice lookup is faster than hash-map
}

func (this *state) advance(in rangeTableId) *state {
	for _, transition := range this.next {
		if transition.in == in {
			return transition.to
		}
	}
	return nil
}

func unicodeRange(r rune) rangeTableId {
	for i := doubleQuote; i <= extendNumLet; i++ {
		if unicode.Is(idToRange[i], r) {
			return i
		}
	}
	return noneOfTheMentioned
}

type rangeTableId int

const (
	noneOfTheMentioned rangeTableId = iota
	doubleQuote
	singleQuote
	hebrewLetter
	cr
	lf
	newline
	extend
	regionalIndicator
	format
	katakana
	aletter
	midLetter
	midNum
	midNumLet
	numeric
	extendNumLet
)

