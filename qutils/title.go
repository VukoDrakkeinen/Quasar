func Title(s string) string {
	runes := []rune(s)
	breaks := wordBreakFSA(runes)
	b := 0
	for i := range runes {
		if b < len(breaks) && i == breaks[b] {
			b++
			runes[i] = unicode.ToTitle(runes[i])
		}
	}
	return string(runes)
}

func Breaks(s string) string {
	runes := []rune(s)
	breaks := wordBreakFSA(runes)
	broken := make([]rune, 0, len(runes)+len(breaks)+1)
	for i, b := range breaks {
		lo := b
		hi := 0
		if i+1 < len(breaks) {
			hi = breaks[i+1]
		} else {
			hi = len(runes)
		}
		broken = append(broken, '|')
		broken = append(broken, runes[lo:hi]...)
	}
	broken = append(broken, '|')
	return string(broken)
}

